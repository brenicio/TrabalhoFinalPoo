    private void txtAdicionarDinheiroMouseExited(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtAdicionarDinheiroMouseExited
        if (!"".equals(txtAdicionarDinheiro.getText())) {
            String sv = txtAdicionarDinheiro.getText();
            String vsf = sv.replace("R$", "").replace(" ", "").replace(".", "").replace(",", ".");
            BigDecimal valor = new BigDecimal(vsf);
            valorAdicionar = valor;
            NumberFormat nf = NumberFormat.getCurrencyInstance(new Locale("pt", "BR"));
            String valorFormatado = nf.format(valor);
            crtl.somenteNumero2(txtAdicionarDinheiro, valorFormatado.length());
            txtAdicionarDinheiro.setText(valorFormatado);
        }
    }//GEN-LAST:event_txtAdicionarDinheiroMouseExited

