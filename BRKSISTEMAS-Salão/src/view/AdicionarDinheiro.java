    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed

        CaixaTO caixaTo = new CaixaTO();
        caixaTo = crtl.consultar("Aberto");
        if ("Aberto".equals(caixaTo.getStatus())) {
            CaixaDAO dao = new CaixaDAO();
            crtl.getEntradaTo().setCodCaixa(caixaTo.getCodCaixa());
            crtl.getEntradaTo().setCodUsuario(1);
            crtl.getEntradaTo().setDataEntracaC(getDate());
            crtl.getEntradaTo().setDescricao("Entrada manual de dinheiro");
            crtl.getEntradaTo().setHora(getTime());
            crtl.getEntradaTo().setTipoMovimentacao("Suprimento de Caixa");
            crtl.getEntradaTo().setValor(valorAdicionar);
            crtl.AdicionarDinheiro2();
            LimparCampos();
            c.preencherTabela();
        } else {
          JOptionPane.showMessageDialog(null,"O caixa não está aberto! Abra o caixa para que possa adicionar dinheiro.");
        }


    }//GEN-LAST:event_btnAdicionarActionPerformed

