    private void txtRetirarDinheiroMouseExited(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtRetirarDinheiroMouseExited
        if (!"".equals(txtRetirarDinheiro.getText())) {
            String sv = txtRetirarDinheiro.getText();
            String vsf = sv.replace("R$", "").replace(" ", "").replace(".", "").replace(",", ".");
            BigDecimal valor = new BigDecimal(vsf);
            valorRetirar = valor;
            NumberFormat nf = NumberFormat.getCurrencyInstance(new Locale("pt", "BR"));
            String valorFormatado = nf.format(valor);
            crtl.somenteNumero2(txtRetirarDinheiro, valorFormatado.length());
            txtRetirarDinheiro.setText(valorFormatado);
        }
    }//GEN-LAST:event_txtRetirarDinheiroMouseExited

