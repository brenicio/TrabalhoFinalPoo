    public void preencherTabela() {
        PagamentoCRTL pagCrtl = new PagamentoCRTL();
        ArrayList<PagamentoTO> dados = new ArrayList();
        ArrayList dados1 = new ArrayList();
        dados = pagCrtl.consultarTodos();
        int i = 0;
        while (i < dados.size()) {
            dados1.add(new Object[]{dados.get(i).getCodformapagamento(), dados.get(i).getDescpagamento(), dados.get(i).getTipopagamento(), dados.get(i).getQtdparcela()});
            i++;

        }
        String[] Colunas = new String[]{"Codigo", "Descricao", "Tipo", "Parcelas"};

        TableMODEL modelo = new TableMODEL(dados1, Colunas);
        jTableFpagamento.setModel(modelo);
        jTableFpagamento.getColumnModel().getColumn(0).setPreferredWidth(80);
        jTableFpagamento.getTableHeader().setReorderingAllowed(false);
        jTableFpagamento.setAutoResizeMode(jTableFpagamento.AUTO_RESIZE_OFF);
    }

