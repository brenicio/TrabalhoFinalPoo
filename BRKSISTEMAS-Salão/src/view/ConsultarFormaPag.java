    private void txtPesquisaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtPesquisaMouseClicked
        if (cbOpcao.getSelectedIndex() == 0) {
            Maiuscula();
        } else {
            somenteNumero();
        }
    }//GEN-LAST:event_txtPesquisaMouseClicked

    private void txtPesquisaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPesquisaKeyReleased
        if (cbOpcao.getSelectedIndex() == 0) {
            preencherTabela2();
        } else {
            preencherTabela3();
        }

        if (txtPesquisa.getText().equalsIgnoreCase("")) {
            preencherTabela();
        }
    }//GEN-LAST:event_txtPesquisaKeyReleased

    private void btnSelecionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelecionarActionPerformed
        int linha = jTableFpagamento.getSelectedRow();
        crtl.getPagamentoTo().setDescpagamento(jTableFpagamento.getValueAt(linha, 1).toString());
        crtl.getPagamentoTo().setCodformapagamento(Integer.parseInt(jTableFpagamento.getModel().getValueAt(linha, 0).toString()));
        crtl.getPagamentoTo().setTipopagamento(jTableFpagamento.getValueAt(linha, 2).toString());
        crtl.getPagamentoTo().setQtdparcela(Integer.parseInt(jTableFpagamento.getValueAt(linha, 3).toString()));

        if (tela.equals("vendaProd")) {
            fpProd.recebendo(crtl.getPagamentoTo());
            this.dispose();
            fpProd.setVisible(true);
        } else {
            fp.recebendo(crtl.getPagamentoTo());
            this.dispose();
            fp.setVisible(true);
        }
    }//GEN-LAST:event_btnSelecionarActionPerformed

