    private void btnRealizarVendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRealizarVendaActionPerformed
        CaixaCRTL crtl = new CaixaCRTL();
        CaixaTO caixaTo = crtl.consultar("Aberto");
        if ("Aberto".equals(caixaTo.getStatus())) {
            btnRealizarVenda.setEnabled(true);
            VendasEfetuadasServico n = new VendasEfetuadasServico(codUsuarioLogado);
            this.escritorio1.add(n);
            n.show();
            vs = this.escritorio1.getSelectedFrame();
        } else {

            JOptionPane.showMessageDialog(null, "E necessário abrir o caixa para realizar uma venda!");
        }


    }//GEN-LAST:event_btnRealizarVendaActionPerformed

    private void btnCaixaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCaixaActionPerformed
        if (this.escritorio1.getSelectedFrame() != null && this.cx.getClass().getName().equalsIgnoreCase("view.Caixa1")) {
            this.escritorio1.setSelectedFrame(cx);
            cx.toFront();
        } else {
            Caixa1 n = new Caixa1(tipoUsuarioLogado);
            this.escritorio1.add(n);
            n.show();
            cx = this.escritorio1.getSelectedFrame();
        }
    }//GEN-LAST:event_btnCaixaActionPerformed

