    private void btnVendaProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVendaProdActionPerformed
        CaixaCRTL crtl = new CaixaCRTL();
        CaixaTO caixaTo = crtl.consultar("Aberto");
        if ("Aberto".equals(caixaTo.getStatus())) {
            VendasEfetuadass vendaprod = new VendasEfetuadass();
            MenuPrincipal.getPainel().add(vendaprod);
            vendaprod.setVisible(true);
            vp = this.escritorio1.getSelectedFrame();

        } else {

            JOptionPane.showMessageDialog(null, "E necessário abrir o caixa para realizar uma venda!");
        }


    }//GEN-LAST:event_btnVendaProdActionPerformed

