    public void LimparCampos() {
        txtDescricao.setText("");
        txtCodigo.setText("");
    }

    public void GanharFoco(final JTextField txt) {
        javax.swing.SwingUtilities.invokeLater(new Runnable() {
            public void run() {
                txt.requestFocusInWindow();
            }
        });
    }

    public void recebendo(ServicoTO servTo) {
        txtCodigo.setText(String.valueOf(servTo.getCodServico()));
        txtDescricao.setText(servTo.getDescrServico());
        servicoTo = servTo;
        GanharFoco(txtDescricao);

        btnAlterar.setEnabled(true);
        btnExcluir.setEnabled(true);
        btnIncluir.setEnabled(false);
    }

