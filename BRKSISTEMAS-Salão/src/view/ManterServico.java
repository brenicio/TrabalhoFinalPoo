    private void btnIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncluirActionPerformed
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja Incluir o Serviço: " + txtDescricao.getText() + "?", title, JOptionPane.YES_NO_OPTION);
        if (resposta == JOptionPane.YES_OPTION) {
            crtl.getServicoTo().setDescrServico(txtDescricao.getText());
            limpar = crtl.incluirServico();
            if (limpar.equals("limpar")) {
                LimparCampos();
            }
            if (limpar.equals("A Descrição do Serviço informada já existe no sistema!")) {
                GanharFoco(txtDescricao);
            }
        }
    }//GEN-LAST:event_btnIncluirActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente alterar o serviço: " + txtDescricao.getText() + "?", title, JOptionPane.YES_NO_OPTION);
        if (resposta == JOptionPane.YES_OPTION) {

            crtl.getServicoTo().setDescrServico(txtDescricao.getText());
            crtl.getServicoTo().setCodServico(Integer.parseInt(txtCodigo.getText()));
            limpar = crtl.alterarServico();
            if (limpar.equals("limpar")) {
                LimparCampos();
                btnIncluir.setEnabled(true);
                btnAlterar.setEnabled(false);
                btnExcluir.setEnabled(false);
            }
            if (limpar.equals("A Descrição do Serviço informada já existe no sistema!")) {
                GanharFoco(txtDescricao);
            }

        } else if (resposta == JOptionPane.NO_OPTION) {

        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja excluir o serviço: " + txtDescricao.getText() + "?", title, JOptionPane.YES_NO_OPTION);
        if (resposta == JOptionPane.YES_OPTION) {
            crtl.excluir(Integer.parseInt(txtCodigo.getText()));
            LimparCampos();
            btnIncluir.setEnabled(true);
            btnAlterar.setEnabled(false);
            btnExcluir.setEnabled(false);
        } else if (resposta == JOptionPane.NO_OPTION) {

        }
    }//GEN-LAST:event_btnExcluirActionPerformed

