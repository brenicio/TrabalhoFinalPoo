    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente alterar a categoria: " + txtDescricao.getText() + "?", title, JOptionPane.YES_NO_OPTION);
        if (resposta == JOptionPane.YES_OPTION) {
            crtl.getCategoriaTo().setCodCategoria(Integer.parseInt((txtCodigo.getText())));
            crtl.getCategoriaTo().setNomeCategoria(txtDescricao.getText());
            crtl.alterarCategoria();
        } else if (resposta == JOptionPane.NO_OPTION) {

        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir a categoria: " + txtDescricao.getText() + "?", title, JOptionPane.YES_NO_OPTION);
        if (resposta == JOptionPane.YES_OPTION) {
            crtl.excluir(Integer.parseInt(txtCodigo.getText()));
        } else if (resposta == JOptionPane.NO_OPTION) {

        }
    }//GEN-LAST:event_btnExcluirActionPerformed
    private void desabilitarTextField() {
        txtCodigo.setEnabled(false);
    }

    private void Maiuscula() {
        crtl.Maiuscula(txtDescricao);
    }

    private void desabitilarBotoes() {
        btnAlterar.setEnabled(false);
        btnExcluir.setEnabled(false);
    }

    private void habilitarBotoes() {
        btnAlterar.setEnabled(true);
        btnExcluir.setEnabled(true);
    }

    public void recebendo(CategoriaTO to) {
        txtCodigo.setValue(to.getCodCategoria());
        txtDescricao.setText(to.getNomeCategoria());
        habilitarBotoes();
    }


